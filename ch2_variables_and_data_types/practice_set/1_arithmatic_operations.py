
a = int(input("Enter number a : "))
b = int(input("Enter number b : "))

print("a+b : ",a+b)
print("a-b : ",a-b)
print("a*b : ",a*b)
print("a/b : ",a/b)
print("a%b : ",a%b)

print("avg(a+b) : ",(a+b)/2)

# finding square
print("a^2 : ",a**2)
print("b^2 : ",b**2)

# finding any power
print("a^3 : ",a**3)
print("b^3 : ",b**3)